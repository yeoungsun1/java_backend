package day02;
//1번 : 단축키, 중괄호 영역 확인
public class Test01 { // 클래스 시작 중괄호 영역
	public static void main(String[] args) { //main메소드 시작 중괄호 영역
		// 한줄 주석 : 설명문, 프로그램 실행 시 영향을 끼치지 않는다
		// ctrl + F11 : 실행 단축키
		// syso + ctrl + space bar : 출력 메소드 자동완성 단축키
		System.out.println("2025년 12월 16일"); 
		// 2025년 12월 16일이라는 문자열을 콘솔창에 출력하고 줄바꿈한다
		System.out.println("2일차 수업 입니다.");
		// 2일차 수업입니다라는 문자열을 콘솔창에 출력하고 줄바꿈한다
	} //main 메소드 끝 중괄호 영역
} //클래스 끝 중괄호 영역
